# /////////////////////////
# // To break a loop... //
# ////////////////////////

# // Use the 'break' keyword
n = 0

# // Since we're using 'while True' this is an infinite loop
while True:
    # // If n is greater than 10, then the loop will end
    if n > 10:
        break
    
    # // Otherwise, print n
    else:
        print(n)
