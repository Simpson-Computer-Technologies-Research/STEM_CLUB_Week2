# // The second loop is a while loop //
# /////////////////////////////////////

# // As long as whatever is put into the statement
# // below is equal to True, then the content inside the
# // loop will continuously run.
while 1 == 1:
    print("1 is equal to 1")
# >> Output: "1 is equal to 1" ... and infinite amount of times
    
# // Once the variable n is no longer less than 5
# // the loop will end.
n = 5
while n < 5:
    print(n)
    n += 1
# >> Output: 0, 1, 2, 3, 4
